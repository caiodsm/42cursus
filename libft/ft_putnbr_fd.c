#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	unsigned int	nb;

	nb = 0;
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nbr;
	}
	else
		nb = nbr;
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd((nb % 10) + 48, fd);
}
